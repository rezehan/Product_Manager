public class Product {
    // Private atribute for storing values
    private String name;
    private int price;

    // Public method to set the name
    public void setName(String name) {
        this.name = name;
    }
    public void setPrice(int price) {
        this.price = price;
    }
    // Public method to get the name
    public String getName() {
        return name;
    }
    public int getPrice() {
        return price;
    }
}